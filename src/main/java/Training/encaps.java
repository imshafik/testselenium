package Training;

public class encaps {
	
	private String name;
	private String idnum ;
	private int age ;
	
	public String getFname() {
		return name;
	}
	public void setFname(String newfname) {
		name = newfname;
	}
	public String getIdnum() {
		return idnum;
	}
	public void setIdnum(String idnum) {
		this.idnum = idnum;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
